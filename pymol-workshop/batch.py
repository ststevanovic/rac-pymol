def query_rcsb_by_category(category: str, n: int = 10) -> list[str]:
    """Return up to *n* PDB IDs matching *category* via RCSB full-text search."""
    keyword = PDB_CATEGORIES.get(category, category)
    payload = json.dumps({
        "query": {
            "type": "terminal",
            "service": "full_text",
            "parameters": {"value": keyword},
        },
        "return_type": "entry",
        "request_options": {
            "paginate": {"start": 0, "rows": n * 3},
            "sort": [{"sort_by": "score", "direction": "desc"}],
        },
    }).encode()
    req = urllib.request.Request(
        RCSB_SEARCH,
        data=payload,
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=FETCH_TIMEOUT) as resp:
            hits = json.loads(resp.read())
        ids = [h["identifier"] for h in hits.get("result_set", [])]
        random.shuffle(ids)
        return ids[:n]
    except Exception as exc:
        print(f"  [WARN]   RCSB search failed for '{category}': {exc}")
        return []


def locate_cif(pdb_id: str, cif_dir: Path | None = None) -> Path | None:
    """Return CIF path: tests/data first, then *cif_dir* (run-local cache),
    then global CIF_CACHE, then download and store in both."""
    local = ROOT / "tests" / "data" / f"{pdb_id.lower()}.cif"
    if local.exists():
        return local

    # run-local timestamped cache dir (preferred store)
    run_cached = (cif_dir / f"{pdb_id.lower()}.cif") if cif_dir else None
    if run_cached and run_cached.exists():
        return run_cached

    global_cached = CIF_CACHE / f"{pdb_id.lower()}.cif"
    if global_cached.exists():
        # copy into run dir as well
        if run_cached:
            run_cached.write_bytes(global_cached.read_bytes())
        return run_cached or global_cached

    url = RCSB_URL.format(pdb_id.upper())
    try:
        print(f"  [fetch]  {url}")
        with urllib.request.urlopen(url, timeout=FETCH_TIMEOUT) as resp:
            data = resp.read()
        global_cached.write_bytes(data)
        if run_cached:
            run_cached.write_bytes(data)
        return run_cached or global_cached
    except Exception as exc:
        print(f"  [WARN]   fetch failed for {pdb_id}: {exc}")
        return None


def write_run_meta(path: Path, meta: dict) -> None:
    """Append / overwrite the run metadata JSON at *path*."""
    path.write_text(json.dumps(meta, indent=2))
    print(f"  [meta]   {path.name} written")


RCSB_ENTRY_URL = "https://www.rcsb.org/structure/{}"


def rcsb_entry_url(pdb_id: str) -> str:
    """Return the canonical RCSB entry URL for *pdb_id* (hardcoded template)."""
    return RCSB_ENTRY_URL.format(pdb_id.upper())


def _build_visual_state(ctrl: "PyMOLController", scene_id: int) -> dict:
    """Build visual_system_state.json content from DB scene data.

    Reads scene meta (global_settings, view_matrix) and scene objects
    (per-BT native representations) from the DB so the Vis slide deck
    reflects the actual scene rather than the static test-data fallback.
    """
    scene    = ctrl.load_scene(scene_id)
    meta     = json.loads(scene["meta"])
    obj_recs = ctrl.load_scene_objects(scene_id)

    objects: dict = {}
    for rec in obj_recs:
        bt      = rec["name"]
        payload = json.loads(rec["payload"])
        bucket  = (payload.get("objects") or {}).get(bt) or {}
        native  = bucket.get("native") or {}

        if bt == "chains":
            # Each sub-key is a chain letter → its own entry in the state objects map
            for chain_letter, chain_bucket in bucket.items():
                if isinstance(chain_bucket, dict):
                    chain_native = chain_bucket.get("native") or {}
                    objects[f"chain_{chain_letter}"] = {
                        "representations": chain_native.get("representations") or {}
                    }
        else:
            objects[bt] = {"representations": native.get("representations") or {}}

    return {
        "objects":         objects,
        "view_matrix":     json.loads(scene["view"]),
        "global_settings": meta.get("global_settings") or {},
    }


# ── main ─────────────────────────────────────────────────────────────────────

def main(ref: str) -> None:
    """Batch pipeline.

    *ref* — the reference PDB ID passed via sys.argv (same one enzyme.py
    styled and stored to DB).  Candidates are queried from RCSB and rendered
    with the same scene from DB.
    """
    _ui = os.environ.get("RAC_LOCAL_UI") == "1"  # set by local.sh
    if _ui:
        start_local_server()

    ctrl = PyMOLController()
    print(f"[batch] DB: {ctrl._db.path}")

    # Timestamped output dir — use pre-minted tag from caller (local.sh) if
    # available, so the UI knows the dir before the run starts.  Falls back to
    # generating its own tag (e.g. on GitHub Actions).
    run_tag = os.environ.get("BATCH_RUN_TAG") or dt.datetime.now().strftime("%Y%m%d_%H%M%S")
    run_dir = RANDOM_DIR / run_tag
    run_dir.mkdir(parents=True, exist_ok=True)
    print(f"[batch] run dir: {run_dir}")

    # ── scene from DB — prefer scene whose name contains the ref PDB ID ──
    all_scenes = ctrl.list_scenes()
    if not all_scenes:
        print("[batch] ERROR: no scenes in DB — run enzyme.py / simple.py first")
        cmd.quit()
        return
    # Pick the scene whose name contains the ref PDB ID (case-insensitive).
    # Fall back to the most recent scene if none match.
    matched = [s for s in all_scenes if ref.lower() in s["name"].lower()]
    scene_row  = matched[-1] if matched else all_scenes[-1]
    scene_id   = scene_row["id"]
    scene_name = scene_row["name"]
    print(f"  [db] scene '{scene_name}' id={scene_id}")

    _source_meta = {
        "template": Path(__file__).name,
        "template_path": str(Path(__file__).relative_to(ROOT.parent)),
        "generated_at": dt.datetime.now().isoformat(timespec="seconds"),
        "category": SELECTED_CATEGORY,
        "scene": scene_name,
        "rcsb_urls": {ref.upper(): rcsb_entry_url(ref)},
    }
    source_path = run_dir / ".source.json"
    source_path.write_text(json.dumps(_source_meta, indent=2))

    # ── Phase A: reference — DB replay ───────────────────────────────────
    print(f"\n[batch] REFERENCE ── {ref}")
    ref_cif = locate_cif(ref, run_dir)
    if ref_cif is None:
        print(f"[batch] ERROR: cannot locate reference CIF {ref}")
        cmd.quit()
        return
    cmd.reinitialize()
    cmd.load(str(ref_cif), ref)
    cmd.viewport(800, 600)
    apply_scene(scene_id=scene_id)
    cmd.png(str(run_dir / f"{ref}_applied.png"), width=800, height=600, ray=1)
    print(f"  ✓ {ref}_applied.png")

    # Copy loaded/staged PNGs from base .rendering dir so slide 1 shows all phases.
    for phase in ("loaded", "staged"):
        for variant in (ref, ref.upper(), ref.lower()):
            src = RENDER_ROOT / f"{variant}_{phase}.png"
            if src.exists():
                shutil.copy2(src, run_dir / f"{ref}_{phase}.png")
                print(f"  ✓ {ref}_{phase}.png  (copied from .rendering/)")
                break

    # ── Phase B: candidates — RCSB query, DB replay ───────────────────────
    print(f"\n[batch] querying RCSB category '{SELECTED_CATEGORY}' …")
    candidate_ids = query_rcsb_by_category(SELECTED_CATEGORY, n=N_SUBJECTS)
    if not candidate_ids:
        print("[batch] WARN: no results from RCSB, falling back to hardcoded list")
        candidate_ids = ["1lp3", "6j6j", "1mbo", "1hew", "1igt"]

    rendered = []
    for idx, pdb_id in enumerate(candidate_ids):
        cif = locate_cif(pdb_id, run_dir)
        if cif is None:
            continue
        print(f"\n[batch] CANDIDATE ── {pdb_id}")
        cmd.reinitialize()
        cmd.load(str(cif), pdb_id)
        cmd.viewport(800, 600)
        apply_scene(scene_id=scene_id)
        cmd.png(str(run_dir / f"{pdb_id}_applied.png"), width=800, height=600, ray=1)
        print(f"  ✓ {pdb_id}_applied.png")
        rendered.append(pdb_id)

    # Patch source.json with final PDB list and all RCSB URLs
    _source_meta["PDB_codes"] = [ref] + rendered
    for pid in rendered:
        _source_meta["rcsb_urls"][pid.upper()] = rcsb_entry_url(pid)
    source_path.write_text(json.dumps(_source_meta, indent=2))

    cmd.quit()

    # ── Phase C: slides ───────────────────────────────────────────────────
    # Write run-specific visual_system_state.json so Vis plot slides reflect
    # the actual scene rather than the static test-data fallback.
    state = _build_visual_state(ctrl, scene_id)
    (run_dir / "visual_system_state.json").write_text(json.dumps(state, indent=2))
    print("  [state]  visual_system_state.json written")

    print("\n[batch] Generating slides …")
    Vis.build_deck(molecules=[ref] + rendered, output_dir=run_dir)

    slides_out = run_dir / "slides.html"
    latest = RANDOM_DIR / "latest.html"
    if slides_out.exists():
        shutil.copy2(slides_out, latest)
        print(f"  [copy]   latest.html ← {run_tag}/slides.html")
        if _ui:
            # Write a done-marker so the UI can detect completion via HEAD request
            (RANDOM_DIR / f"{run_tag}.done").write_text(run_tag)
    else:
        print("[batch] WARN: slides.html not found, skipping copy")
    print("[batch] Done.")


if __name__ == "__main__":
    # Usage: python batch.py <ref_pdb_id>
    # ref_pdb_id — the PDB code enzyme.py was run on (e.g. 9ax6)
    if len(sys.argv) != 2:
        print("Usage: python batch.py <ref_pdb_id>")
        sys.exit(1)
    main(sys.argv[1].lower())
