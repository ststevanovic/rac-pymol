    @staticmethod
    def _img_b64(path: Path) -> str:
        with open(path, "rb") as f:
            return "data:image/png;base64," + base64.b64encode(f.read()).decode()

    @classmethod
    def _resolve_state(cls, output_dir: Path | None = None) -> dict:
        candidates = []
        if output_dir:
            candidates.append(Path(output_dir) / "visual_system_state.json")
        candidates.append(cls._STATE_DEFAULT)
        for p in candidates:
            if p.exists():
                return json.loads(p.read_text())
        raise FileNotFoundError(f"visual_system_state.json not found. Tried: {candidates}")

    @classmethod
    def _gs_category(cls, key: str) -> str:
        kl = key.lower()
        for cat, keywords in cls._GS_CATEGORIES:
            if any(kw in kl for kw in keywords):
                return cat
        return "Other"

