    # ── image slide assembly ──────────────────────────────────────────────

    @classmethod
    def _find_png(cls, img_dir: Path, stem: str) -> Path | None:
        for variant in (stem, stem.upper(), stem.lower()):
            p = img_dir / f"{variant}_applied.png"
            if p.exists():
                return p
        return None

    @classmethod
    def _image_slides(cls, reference: str, subjects: list[str], img_dir: Path) -> list:
        src_file = img_dir / ".source.json"
        rcsb_urls: dict = {}
        if src_file.exists():
            try:
                rcsb_urls = json.loads(src_file.read_text()).get("rcsb_urls", {})
            except Exception:
                pass

        slides = []
        ref_panels = {}
        for phase in ("loaded", "applied"):
            for variant in (reference, reference.upper(), reference.lower()):
                p = img_dir / f"{variant}_{phase}.png"
                if p.exists():
                    ref_panels[phase] = p
                    break
        if ref_panels:
            weights = [1] + [2] * (len(ref_panels) - 1)
            slides.append(cls.slide_compare(
                reference.upper(), ref_panels,
                col_weights=weights,
                subtitle=rcsb_urls.get(reference.upper()),
            ))

        ref_col_img = None
        for phase in ("staged", "applied"):
            for variant in (reference, reference.upper(), reference.lower()):
                p = img_dir / f"{variant}_{phase}.png"
                if p.exists():
                    ref_col_img = p
                    break
            if ref_col_img:
                break

        for mol in subjects:
            panels = {}
            if ref_col_img:
                panels["ORIGINAL SCENE"] = ref_col_img
            p = cls._find_png(img_dir, mol)
            if p:
                panels[f"{mol.upper()}  ·  RCSB PDB"] = p
            if panels:
                slides.append(cls.slide_compare(
                    mol.upper(), panels,
                    subtitle=rcsb_urls.get(mol.upper()),
                ))
        return slides

