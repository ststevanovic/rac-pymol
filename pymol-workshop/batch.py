    @classmethod
    def slide_compare(cls, title: str, paths: dict,
                      col_weights: list[int] | None = None,
                      subtitle: str | None = None):
        ncols = len(paths)
        grid_cols = (
            " ".join(f"{w}fr" for w in col_weights)
            if col_weights and len(col_weights) == ncols
            else " ".join("1fr" for _ in range(ncols))
        )
        subtitle_html = (
            f'\n<div class="img-slide-subtitle"><a href="{subtitle}" target="_blank">Crystal ID</a></div>'
            if subtitle
            else '\n<div class="img-slide-subtitle">Crystal ID</div>'
        )
        cols_html = ""
        for label, path in paths.items():
            src = cls._img_b64(path)
            is_applied = label.lower() == "applied"
            col_cls = "img-col img-col--applied" if is_applied else "img-col"
            badge = '\n    <div class="db-badge">&#x2713;&nbsp;WRITTEN TO SCNgraphy</div>' if is_applied else ""
            cols_html += (
                f'\n  <div class="{col_cls}">'
                f'\n    <div class="col-label" title="{label}">{label}</div>'
                f'\n    <div class="img-wrap"><img src="{src}" alt="{label}"></div>{badge}'
                f'\n  </div>'
            )
        html = (
            f'\n<div class="img-slide-title">{title}</div>{subtitle_html}'
            f'\n<div class="img-row" style="grid-template-columns:{grid_cols};">{cols_html}\n</div>'
        )
        return ("html", title, html)

