    @classmethod
    def slide_global_settings(cls, state: dict):
        import plotly.graph_objects as go

        def slog(v):
            return math.copysign(math.log1p(abs(v)), v)

        gs = state["global_settings"]

        _HIDDEN_CATS = {"Other", "UI", "IO/PDB"}

        # Collect numeric rows and classify (skip hidden categories)
        raw_rows: list[dict] = []
        for k, v in gs.items():
            try:
                cat = cls._gs_category(k)
                if cat in _HIDDEN_CATS:
                    continue
                raw_rows.append({"key": k, "val": float(v), "cat": cat})
            except (ValueError, TypeError):
                pass

        # Group by category → keep top 3 by abs(val) → clamp to [-100, 100]
        by_cat: dict = defaultdict(list)
        for r in raw_rows:
            by_cat[r["cat"]].append(r)

        filtered: list[dict] = []
        for cat in sorted(by_cat):
            top3 = sorted(by_cat[cat], key=lambda r: -abs(r["val"]))[:3]
            for r in top3:
                clamped = max(-100.0, min(100.0, r["val"]))
                filtered.append({"key": r["key"], "val": r["val"], "clamped": clamped, "cat": cat})

        # Assign y positions (gap between categories)
        y_pos: list[float] = []
        y_labels: list[str] = []
        y, prev_cat = 0.0, None
        for r in filtered:
            if r["cat"] != prev_cat:
                if prev_cat is not None:
                    y += 2.0
                prev_cat = r["cat"]
            else:
                y += 1.0
            y_pos.append(y)
            y_labels.append(r["key"])

        # Group by cat for plotting
        cat_items: dict = defaultdict(list)
        cat_y: dict = defaultdict(list)
        for i, r in enumerate(filtered):
            cat_items[r["cat"]].append((i, r))
            cat_y[r["cat"]].append(y_pos[i])

        fig = go.Figure()

        # Alternating band backgrounds
        for band_i, (cat, ys) in enumerate(cat_y.items()):
            if band_i % 2 == 0:
                fig.add_hrect(y0=min(ys) - 0.5, y1=max(ys) + 0.5,
                               fillcolor="rgba(255,255,255,0.03)", line_width=0, layer="below")

        fig.add_vline(x=0, line=dict(color=cls.MUTE, dash="dot", width=1))

        seen: set = set()
        for cat, items in cat_items.items():
            color = cls._CAT_COLORS.get(cat, cls.GRID)
            show_legend = cat not in seen
            seen.add(cat)
            for j, (i, r) in enumerate(items):
                was_clamped = abs(r["val"]) > 100.0
                fig.add_trace(go.Scatter(
                    x=[slog(r["clamped"])], y=[y_pos[i]], mode="markers",
                    marker=dict(
                        size=13,
                        color=color,
                        opacity=0.85,
                        symbol="triangle-right" if was_clamped else "circle",
                        line=dict(color=cls.BG, width=1),
                    ),
                    name=cat, legendgroup=cat, showlegend=show_legend and j == 0,
                    text=[r["key"]],
                    customdata=[[cat, r["val"], was_clamped]],
                    hovertemplate=(
                        "<b>%{text}</b><br>%{customdata[0]}<br>"
                        "value: %{customdata[1]:.4g}"
                        "%{customdata[2]:<br><i>clamped to ±100</i>}<extra></extra>"
                    ),
                ))

        # Category labels on the right
        for cat, ys in cat_y.items():
            mid = (min(ys) + max(ys)) / 2
            fig.add_annotation(
                x=1.02, y=mid, xref="paper", yref="y",
                text=f"<b>{cat}</b>", showarrow=False,
                font=dict(size=12, color=cls._CAT_COLORS.get(cat, cls.GRID)),
                xanchor="left",
            )

        tick_raw = [-100, -10, -1, 0, 1, 10, 100]
        fig.update_layout(
            title=dict(
                text="Render Parameters  ·  by Category",
                font=dict(size=20, color=cls.FG),
                xref="paper", x=0, xanchor="left",
                yref="paper", y=1.0, yanchor="bottom", pad=dict(b=12),
            ),
            xaxis=dict(
                title=dict(text="SignedLog", font=dict(size=13, color=cls.MUTE)),
                tickvals=[slog(v) for v in tick_raw],
                ticktext=[str(v) for v in tick_raw],
                tickfont=dict(size=12, color=cls.FG),
                gridcolor=cls.GRID, zeroline=False,
            ),
            yaxis=dict(
                tickvals=y_pos, ticktext=y_labels,
                tickfont=dict(size=11, color=cls.FG),
                gridcolor=cls.GRID, showgrid=False,
            ),
            showlegend=False,
            paper_bgcolor=cls.BG, plot_bgcolor=cls.SURF,
            margin=dict(l=220, r=120, t=36, b=50),
        )
        return ("plotly", "Global Settings", fig)

